import Foundation
import UIKit

class CVColor: NSObject {
    
    class func lightGreenColor() -> UIColor {
        
        return UIColor(red: 127/255.0, green: 255/255.0, blue: 123/255.0, alpha: 1)
        
    }
    
    class func blueColor() -> UIColor {
        
        return UIColor(red: 122/255.0, green: 211/255.0, blue: 251/255.0, alpha: 1)
        
    }
    
    class func redColor() -> UIColor {
        
        return UIColor(red: 250/255.0, green: 114/255.0, blue: 108/255.0, alpha: 1)
    }
    
    class func whiteColor() -> UIColor {
        
        return UIColor(red: 239/255.0, green: 239/255.0, blue: 239/255.0, alpha: 1)
        
}
}