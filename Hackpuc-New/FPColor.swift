import Foundation
import UIKit

class FPColor: NSObject {
    
    // green etc..
    class func gColor() -> UIColor {
        
        return UIColor(red: 127/255.0, green: 255/255.0, blue: 123/255.0, alpha: 1)
        
    }
    
    class func bColor() -> UIColor {
        
        return UIColor(red: 52/255.0, green: 181/255.0, blue: 240/255.0, alpha: 1)
        
    }
    
    class func rColor() -> UIColor {
        
        return UIColor(red: 250/255.0, green: 114/255.0, blue: 108/255.0, alpha: 1)
    }
    
    class func wColor() -> UIColor {
        
        return UIColor(red: 239/255.0, green: 239/255.0, blue: 239/255.0, alpha: 1)
        
}
}